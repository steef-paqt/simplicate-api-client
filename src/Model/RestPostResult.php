<?php

declare(strict_types=1);

namespace Paqtcom\Simplicate\Model;

class RestPostResult extends AbstractRestResult
{
    protected string $id;

    /**
     * @return string
     */
    public function getId(): string
    {
        return $this->id;
    }

    /**
     * @param string $id
     *
     * @return self
     */
    public function setId(string $id): self
    {
        $this->initialized['id'] = true;
        $this->id = $id;

        return $this;
    }
}
